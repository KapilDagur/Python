#Here we see Arithmatic operation
#Firstly we see Arithmatic operation Between Interger
val1 = 8
val2 = 10
result = val1 + val2 #Addition Operation between interger, Expected result=> 8 + 10 = 18
print(result)
result = val1 - val2 #Substract Operation between integer, Expected result=> 8 - 10 = -2
print(result)
result = val1 * val2 #Multiplecation Operation between integer, Expected result=> 8 * 10 = 80
print(result)
result = val1 / val2 #Divide Operation between integer, Expected result=> 8 / 10 = 0.8
print(result)
result = val2 // val1 #Integer Divide Operation between integer, Expected result=> 10 // 8 = 1
print(result)  #Note when we use Interger Divide then we get integer value as result bcz 10 / 8 = 1.25 but integer part is 1 hence 1 is answer
result = val1 % val2 #modulo Operation between integer(modulo operation just be arithmetic whose give us reminder), Expected result=> 8 % 10 = 8
print(result)
